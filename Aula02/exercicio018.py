# Desenvolva um programa que peça ao usuário para inserir um valor booleano e, em seguida, inverta esse valor.

# Solicita ao usuário que insira um valor booleano
valor_usuario = input("Insira um valor booleano (True/False): ")

# Converte a entrada do usuário para um valor booleano
resultado = not(valor_usuario)

# Exibe o valor booleano invertido
print("O valor booleano invertido é:", resultado)

# Escreva um programa que avalie duas expressões booleanas inseridas pelo usuário e retorne o resultado da operação AND entre elas.

# Insirir uma expressão booleana
expr1 = input("Insira a primeira expressão booleana (ex: True, False, 5 > 3): ")

# Insirir outra expressão booleana: ")
expr2 = input("Insira a segunda expressão booleana (ex: True, False, 5 > 3): ")

# Avaliar as expressões
resultado = expr1 and expr2

# Exibir o resultado da operação AND
print("O resultado da operação AND entre as expressões é:", resultado)

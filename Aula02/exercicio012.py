# Crie um programa que receba o nome completo do usuário e imprima o nome com todas as letras minúsculas.

# Digitar o nome completo do usuário
nome_completo = input("Digite seu nome completo: ")

# Converter o nome para letras minúsculas
nome_minusculo = nome_completo.lower()

# Imprimir o nome em letras minúsculas
print(f'Nome em letras minúsculas: {nome_minusculo}')

# Escreva um programa que receba dois números flutuantes e realize sua adição.

# Digitar um número de ponto flutuante
num1 = input("Digite um número de ponto flutuante: ")

# Digitar outro número de ponto flutuante
num2 = input("Digite outro número de ponto flutuante: ")

# Somar os dois números de ponto flutuante
soma = float(num1) + float(num2)

# Imprimir o resultado da soma
print(f"A soma dos dois números é: {soma}")

# Observação: Este código não lida com entradas inválidas ou valores negativos.

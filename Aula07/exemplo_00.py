def soma(valor_1: float, valor_2: float) -> float:
    '''
    Uma função simples de soma de valores do tipo float que retorna float
    '''
    
    valor_3 = valor_1 + valor_2
    return valor_3

numero_1 = 4
numero_2 = 6

resultado_1 = soma(numero_1, numero_2)
print(resultado_1)

numero_3 = 7
numero_4 = 2

resultado_2 = soma(numero_3, numero_4)
print(resultado_2)
